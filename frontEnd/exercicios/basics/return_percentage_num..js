// Write a function that takes two numbers (a and b) as argument
// Return b percent of a
function myFunction(a, b) {
    return a * (b / 100);
}

console.log(myFunction(100, 50));

// -------------------------------------
function myFunction2(a, b) {
    return (b / 100) * a;
}

console.log(myFunction2(100, 50));
